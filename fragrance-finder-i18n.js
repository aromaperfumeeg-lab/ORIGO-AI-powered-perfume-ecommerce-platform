export const fragranceFinderCatalog = { ar, en };

export function translateFinder(language, key, variables = {}) {
  const languageCatalog = fragranceFinderCatalog[language] || fragranceFinderCatalog.ar;
  let value = languageCatalog[key];
  if (value == null) {
    if (typeof console !== "undefined" && console.warn) console.warn(`[ORIGO i18n] Missing ${language}:${key}`);
    value = fragranceFinderCatalog.ar[key] ?? key;
  }
  return String(value).replace(/\{(\w+)\}/g, (_, name) => variables[name] ?? `{${name}}`);
}

export function catalogKeyDifferences() {
  const arKeys = Object.keys(ar);
  const enKeys = Object.keys(en);
  return {
    missingInArabic: enKeys.filter((key) => !(key in ar)),
    missingInEnglish: arKeys.filter((key) => !(key in en))
  };
}

if (typeof window !== "undefined") {
  window.ORIGOFragranceFinderI18n = {
    catalog: fragranceFinderCatalog,
    translate: translateFinder,
    differences: catalogKeyDifferences
  };
}
